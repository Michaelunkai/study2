# Loops (for and while)
# For loop to print numbers from 1 to 5
print('Using for loop:')

# While loop to countdown from 5 to 1
print('\nUsing while loop:')
countdown = 5
while countdown > 0:
    print(countdown)
    countdown -= 1

# output:

# Using for loop:

# Using while loop:
# 5
# 4
# 3
# 2
# 1