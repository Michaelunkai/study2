import os
import shutil
from collections import defaultdict

def get_all_files(directory):
    """Return a list of all files in the given directory."""
    return [f for f in os.listdir(directory) if os.path.isfile(os.path.join(directory, f))]

def copy_file_to_target_folder(file_path, target_folder):
    """Copy the given file to the target folder."""
    file_name = os.path.basename(file_path)
    os.makedirs(target_folder, exist_ok=True)
    shutil.copy2(file_path, os.path.join(target_folder, file_name))
    print(f"Copied: {file_name} to {target_folder}")

def organize_files(downloads_path, study_path, target_folders_mapping):
    """Organize files in the downloads folder to their corresponding target folders."""
    files = get_all_files(downloads_path)
    copied_files = defaultdict(list)

    print(f"Processing {len(files)} files")

    # Copy files to their target folders
    for file in files:
        file_path = os.path.join(downloads_path, file)
        for keyword, target_folder in target_folders_mapping.items():
            if keyword.lower() in file.lower():
                copy_file_to_target_folder(file_path, target_folder)
                copied_files[target_folder].append(file)

    # Delete original files after they have been copied
    for file in files:
        file_path = os.path.join(downloads_path, file)
        try:
            os.remove(file_path)
            print(f"Deleted: {file}")
        except FileNotFoundError:
            print(f"File {file} has already been deleted.")

    print("File organization completed.")
    return dict(copied_files)

